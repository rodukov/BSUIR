[x, y] = meshgrid(-3:0.1:3, -3:0.1:3);   
% создаём сетку точек (матрицы X и Y) в диапазоне от -3 до 3 с шагом 0.1

z1 = myfunc2_1(x, y);   
z2 = myfunc2_2(x, y);   

contour(x, y, z1, [0 0], 'r');   
% строим контур (линии уровня) для z1(x,y), только по уровню 0, красным цветом

hold on;   
% удерживаем график, чтобы поверх него добавить второй контур

contour(x, y, z2, [0 0], 'b');   
% строим контур для z2(x,y), только по уровню 0, синим цветом

xlabel('x');   
% подпись оси X

ylabel('y');   
% подпись оси Y

title('Контурные графики z1=0 и z2=0');   
% заголовок графика

grid on;   
% включаем сетку на графике
